    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            stmt = con.createStatement();
            if (edtNim.getText().equals("")
                    || edtNamapeminjam.getText().equals("")
                    || edtTanggal.getText().equals("")
                    || edtPukul.getText().equals("")
                    || edtRuangan.getText().equals("")
                    || edtNamakegiatan.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            // Aksi Simpan Data
            if (btnSimpan.getText() == "SIMPAN") {
                String cek = "SELECT * FROM ruangan WHERE nim = '" + edtNim.getText() + "'";
                rs = stmt.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Ups.. NIM sudah ada");
                    return;
                } else {
                
                String sql = "INSERT INTO ruangan VALUES ('" + edtNim.getText()
                        + "','" + edtNamapeminjam.getText()
                        + "','" + edtTanggal.getText()
                        + "','" + edtPukul.getText()
                        + "','" + edtRuangan.getText()
                        + "','" + edtNamakegiatan.getText() + "')";
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                Bersih();
                TampilData();
                }
            } else {
                // Aksi Ubah Data
                String update = "UPDATE ruangan SET namapeminjam = '" + edtNamapeminjam.getText()
                        + "', tanggal = '" + edtTanggal.getText()
                        + "', pukul = '" + edtPukul.getText()
                        + "', ruangan = '" + edtRuangan.getText()
                        + "', kegiatan = '" + edtNamakegiatan.getText()
                        + "' WHERE nim = '" + edtNim.getText() + "'";
                stmt.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                Bersih();
                TampilData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

