    private void CariData() {
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM ruangan WHERE "
                    + cmbCari.getSelectedItem().toString()
                    + " LIKE '%" + edtCari.getText() + "%'");

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("nim");
            model.addColumn("namapeminjam");
            model.addColumn("tanggal");
            model.addColumn("pukul");
            model.addColumn("ruangan");
            model.addColumn("kegiatan");

            int no = 1;

            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("nim"),
                    rs.getString("namapeminjam"),
                    rs.getString("tanggal"),
                    rs.getString("pukul"),
                    rs.getString("ruangan"),
                    rs.getString("kegiatan")
                };
                model.addRow(data);
                tblData.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

